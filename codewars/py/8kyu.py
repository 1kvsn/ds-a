# 1. Given two integer arrays a, b, both of length >= 1, create a program that returns true if the sum of the squares of each element in a is strictly greater than the sum of the cubes of each element in b.

from functools import reduce

def array_madness(a,b):
    return print(sum(list(map(lambda x: x**2, a))) >= sum(list(map(lambda x: x**3, b))))

array_madness([1, 2, 3], [4, 5, 6]) # False

